#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*ts1;
	unsigned char	*ts2;

	i = 0;
	ts1 = (unsigned char *)s1;
	ts2 = (unsigned char *)s2;
	if (!n)
		return (0);
	while (ts1[i] == ts2[i] && i < n - 1)
		i++;
	return ((ts1)[i] - (ts2)[i]);
}
